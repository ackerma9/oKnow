using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace OKnow.UI
{
    /// <summary>
    /// Interface of an IO event
    /// </summary>
    public interface IOEvent
    {
        /// <summary>
        /// Returns true if the event has occured
        /// </summary>
        /// <param name="current">current IOState</param>
        /// <param name="previous">previous IOState</param>
        /// <returns>True if the event has occured</returns>
        Boolean HasOccured(IOState current, IOState previous);
    }
}
